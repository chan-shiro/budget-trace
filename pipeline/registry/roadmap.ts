//
// /roadmap ページに出る。**公開サイトに載る**（誰でも読める）ので、次の3点を守ること:
//
// 1. **時期を約束しない**。「いつ」ではなく「何を・なぜ・何が要るか」を書く。
//    一次資料の入手可否は発行元次第で、我々にコントロールできない（情報公開請求・資料の消失）。
// 2. **進捗の数字をここに書かない**。件数・年度・カバレッジは coverage.json と同じ実データから
//    derive が算出する。ここに数字を書くと必ず実態とズレる（2026-07-15 に2回踏んだ:
//    フッターの「SOURCE: 甲府市 当初予算資料 R2–R8」固定表示、/coverage の予算列が最新年度のみ）。
// 3. **done は書かない**。「できたこと」は実データから導出される（＝/coverage と進捗サマリが答える）。
//    ここに done を書くと二重管理になる。
//
// 根拠・調査結果は docs/data-sources.md に書く。ここは「次に何をするか」の1行説明だけ。

export type RoadmapStatus = "now" | "next" | "later";

export interface RoadmapItem {
  /** 短い見出し（体言止め） */
  title: string;
  status: RoadmapStatus;
  /** なぜやるか（利用者から見た価値）。1〜2文 */
  why: string;
  /** 何が要るか・何が難しいか。技術的な障壁や、我々の手に負えない事情を正直に書く */
  needs: string;
  /** 参照する docs のセクション（任意） */
  ref?: string;
}

